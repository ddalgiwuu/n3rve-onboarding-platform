// Helper functions
export const getLanguageByCode = (code: string): Language | undefined => {
  return SUPPORTED_LANGUAGES.find(lang => lang.code === code);
};

export const getLanguageDisplay = (language: Language): string => {
  return `${language.nativeName} (${language.name})`;
};

export const sortLanguagesByName = (languages: Language[]): Language[] => {
  return [...languages].sort((a, b) => a.name.localeCompare(b.name));
};

export const getCommonLanguages = (): Language[] => {
  const commonCodes = ['en', 'ko', 'ja', 'zh-CN', 'zh-TW', 'es', 'fr', 'de', 'it', 'pt', 'ru', 'ar'];
  return commonCodes.map(code => getLanguageByCode(code)).filter(Boolean) as Language[];
};