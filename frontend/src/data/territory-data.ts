// Helper function to get all countries as a flat array
export const getAllCountries = (): Country[] => {
  return TERRITORY_DATA.flatMap(continent => continent.countries);
};

// Helper function to get countries by continent
export const getCountriesByContinent = (continentCode: string): Country[] => {
  const continent = TERRITORY_DATA.find(c => c.code === continentCode);
  return continent ? continent.countries : [];
};

// Helper function to get country by code
export const getCountryByCode = (code: string): Country | undefined => {
  return getAllCountries().find(country => country.code === code);
};