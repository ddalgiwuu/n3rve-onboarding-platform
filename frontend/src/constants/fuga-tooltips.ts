export const getTooltip = (fieldKey: keyof typeof FUGA_FIELD_TOOLTIPS, language: 'ko' | 'en' = 'ko') => {
  const tooltip = FUGA_FIELD_TOOLTIPS[fieldKey];
  if (!tooltip) return null;

  return {
    title: tooltip.title,
    description: typeof tooltip.description === 'object'
      ? tooltip.description[language]
      : tooltip.description,
    note: tooltip.note ? tooltip.note[language] : undefined,
  };
};
